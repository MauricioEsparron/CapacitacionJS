let a = 10;
console.log(a > 5);   //true
console.log(a >= 5);  //true
console.log(a < 5);   //false
console.log(a <= 5);  //false

console.log(a == 5);  //false
console.log(a != 5);  //true
console.log(a === 5); //false // valida tanto el valor como el tipo de dato
console.log(a !== 5); //true  // valida si es distinto tanto el valor como el tipo de dato
